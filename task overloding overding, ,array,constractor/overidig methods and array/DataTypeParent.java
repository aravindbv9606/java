class DataTypeParent {

    public void show(int value) {
       System.out.println("Parent Float: " + value);
    }

    public void show(float number) {
        System.out.println("Parent Float: " + number);
    }

    public void show(long temp) {
        System.out.println("Parent Long: " + temp);
    }

    public void show(double value) {
        System.out.println("Parent Double: " + value);
    }

    public void show(char value) {
        System.out.println("Parent Char: " + value);
    }

    public void show(boolean value) {
        System.out.println("Parent Boolean: " + value);
    }

    public void show(String value) {
        System.out.println("Parent String: " + value);
    }
}


