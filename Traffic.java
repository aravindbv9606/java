class Traffic {

    public static int setSignalCount(int count) {
        return count;
    }

    public static float setAverageSpeed(float speed) {
        return speed;
    }

    public static char setTrafficCode(char code) {
        return code;
    }

    public static double setFineAmount(double fine) {
        return fine;
    }

    public static long setVehicleCount(long vehicles) {
        return vehicles;
    }

    public static boolean setIsHeavy(boolean isHeavy) {
        return isHeavy;
    }

    public static String setCity(String city) {
        return city;
    }
}