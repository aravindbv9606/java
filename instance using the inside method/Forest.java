class Forest {
    private String forestName = "Elephant";
    private byte treeCount = 10;
    private short animalCount = 100;
    private int forestId = 123;
    private long totalArea = 1000L;
    private float waterLevel = 50.5f;
    private double rainfall = 300.75;
    private char zone = 'A';
    private boolean isProtected = true;

    // Getters
    public String getForestName() {
        return forestName;
    }

    public byte getTreeCount() {
        return treeCount;
    }

    public short getAnimalCount() {
        return animalCount;
    }

    public int getForestId() {
        return forestId;
    }

    public long getTotalArea() {
        return totalArea;
    }

    public float getWaterLevel() {
        return waterLevel;
    }

    public double getRainfall() {
        return rainfall;
    }

    public char getZone() {
        return zone;
    }

    public boolean getIsProtected() {
        return isProtected;
    }
}
